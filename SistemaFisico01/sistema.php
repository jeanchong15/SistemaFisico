        <h1 class="animate__bounce titulo" >¿Qué es el Sistema Físico?</h1>
        <hr>
        <br>

        <div class="p" >
            <p>
                El sistema físico se refiere a un conjunto de elementos interrelacionados que
                interactúan entre sí para realizar funciones específicas en el ámbito físico o
                material. Este término abarca una amplia variedad de contextos, ya que los 
                sistemas físicos pueden encontrarse en diversas disciplinas, como la física,
                la ingeniería, la biología y otras ciencias aplicadas.
            </p>
        </div>

        <br>

        <div class="p" >
            <p>
                En la física, un sistema físico es un conjunto de elementos que se relacionan
                entre sí y que se encuentran en un espacio determinado. Estos elementos pueden
                ser objetos, sustancias, partículas, etc. El sistema físico se caracteriza por
                tener una frontera que lo separa del entorno, y por tener una serie de 
                propiedades que lo definen. Estas propiedades pueden ser: 
            </p>
        </div>
        
        <br>
        <div>
            <li>La forma de movimiento</li>
            <li>La forma de contacto</li> 
            <li>La forma de energía</li>
            <li>La forma de física</li>
            <li>La forma de magnetismo</li>
            <li>La forma de electricidad</li> 
            <li>La forma de circuitos</li>
            <li>La forma de circuitos electrónicos</li>
        </div>

        <br>

        <div class="imgfisica" >
            <img src="imagenes/imgfisica1.jpeg" alt="">
        </div>

        <br>

        <div class="p" >
            <p>
            En el ámbito de la física, un sistema físico puede ser cualquier objeto o
            conjunto de objetos que esté sujeto a ciertas leyes y principios físicos.
            Esto puede incluir desde partículas subatómicas hasta sistemas estelares 
            completos. La descripción y comprensión de estos sistemas a menudo implican
            el uso de teorías y modelos matemáticos para prever su comportamiento y entender
            las interacciones entre sus componentes.
            </p>
        </div>

        <br>

        <div class="p" >
            <p>
            En ingeniería, un sistema físico puede referirse a dispositivos, máquinas o
            procesos que tienen una existencia tangible y que realizan funciones específicas.
            Por ejemplo, un automóvil, una central eléctrica o un sistema de control de temperatura
            en un edificio son ejemplos de sistemas físicos en el contexto de la ingeniería.
            </p>
        </div>

        <br>

        <div class="p" >
            <p>
            En biología, un sistema físico puede referirse a un conjunto de órganos que trabajan
            juntos para realizar una función específica en un organismo. Por ejemplo, el sistema
            digestivo, el sistema nervioso y el sistema circulatorio son ejemplos de sistemas físicos
            en el contexto de la biología.
            </p>
        </div>

        <br>

        <div class="p" >
            <p>
            En ciencias de la computación, un sistema físico puede referirse a un conjunto de
            componentes de hardware que trabajan juntos para realizar una función específica.
            Por ejemplo, un sistema de computadora, un sistema de comunicación o un sistema de
            control de tráfico son ejemplos de sistemas físicos en el contexto de la informática.
            </p> 
        </div>
            
        <br>

        <div class="p" >
            <p>
            En el contexto de la teoría de sistemas, un sistema físico puede referirse a un
            sistema que se encuentra en el mundo físico, en contraste con un sistema abstracto
            que se encuentra en el mundo conceptual. Por ejemplo, un sistema de control de
            tráfico es un sistema físico, mientras que un sistema de control de tráfico es un
            sistema abstracto.
            </p> 
        </div>
            
